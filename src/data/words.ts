export const WORD_CATEGORIES = [
  "Animales",
  "Comida",
  "Objetos",
  "Lugares",
  "Profesiones",
  "Acciones",
  "Deportes",
  "Naturaleza",
] as const

export type WordCategory = (typeof WORD_CATEGORIES)[number]

export type WordDifficulty = "basic" | "extended"

export type WordEntry = {
  value: string
  categories: WordCategory[]
  difficulty: WordDifficulty
}

function toWordEntries(params: { words: string; categories: WordCategory[]; difficulty: WordDifficulty }) {
  const raw = params.words
    .split("\n")
    .map((w) => w.trim())
    .filter(Boolean)

  const seen = new Set<string>()

  return raw
    .map((value) => value.toLowerCase())
    .filter((value) => {
      if (seen.has(value)) return false
      seen.add(value)
      return true
    })
    .map<WordEntry>((value) => ({
      value,
      categories: params.categories,
      difficulty: params.difficulty,
    }))
}

